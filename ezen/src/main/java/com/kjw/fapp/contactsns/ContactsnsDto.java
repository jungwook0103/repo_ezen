package com.kjw.fapp.contactsns;

public class ContactsnsDto {
	
	private String snsseq;
	private String section;
	private String sns;
	private String contact_contactseq;
	private String main;
	
	public String getSnsseq() {
		return snsseq;
	}
	public void setSnsseq(String snsseq) {
		this.snsseq = snsseq;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getSns() {
		return sns;
	}
	public void setSns(String sns) {
		this.sns = sns;
	}
	public String getContact_contactseq() {
		return contact_contactseq;
	}
	public void setContact_contactseq(String contact_contactseq) {
		this.contact_contactseq = contact_contactseq;
	}
	public String getMain() {
		return main;
	}
	public void setMain(String main) {
		this.main = main;
	}
	
	

}
