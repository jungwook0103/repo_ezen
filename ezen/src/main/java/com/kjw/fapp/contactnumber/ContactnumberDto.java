package com.kjw.fapp.contactnumber;

public class ContactnumberDto {
	
	private String conNumseq; 
	private String number;
	private String section;
	private String contact_contactseq;
	private String main;
	
	public String getConNumseq() {
		return conNumseq;
	}
	public void setConNumseq(String conNumseq) {
		this.conNumseq = conNumseq;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getContact_contactseq() {
		return contact_contactseq;
	}
	public void setContact_contactseq(String contact_contactseq) {
		this.contact_contactseq = contact_contactseq;
	}
	public String getMain() {
		return main;
	}
	public void setMain(String main) {
		this.main = main;
	}
	
	

}
