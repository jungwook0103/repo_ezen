package com.kjw.fapp.contactadd;

public class ContactaddDto {
	
	private String addseq;
	private String section;
	private String zipcode;
	private String address;
	private String contact_contactseq;
	private String main;
	
	public String getAddseq() {
		return addseq;
	}
	public void setAddseq(String addseq) {
		this.addseq = addseq;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContact_contactseq() {
		return contact_contactseq;
	}
	public void setContact_contactseq(String contact_contactseq) {
		this.contact_contactseq = contact_contactseq;
	}
	public String getMain() {
		return main;
	}
	public void setMain(String main) {
		this.main = main;
	}
	

}
