package com.kjw.fapp.contactanniv;

public class ContactannivDto {
	
	private String annivseq;
	private String section;
	private String date;
	private String solarLunar;
	private String contact_contactseq;
	private String main;
	
	public String getAnnivseq() {
		return annivseq;
	}
	public void setAnnivseq(String annivseq) {
		this.annivseq = annivseq;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getSolarLunar() {
		return solarLunar;
	}
	public void setSolarLunar(String solarLunar) {
		this.solarLunar = solarLunar;
	}
	public String getContact_contactseq() {
		return contact_contactseq;
	}
	public void setContact_contactseq(String contact_contactseq) {
		this.contact_contactseq = contact_contactseq;
	}
	public String getMain() {
		return main;
	}
	public void setMain(String main) {
		this.main = main;
	}
	
	

}
